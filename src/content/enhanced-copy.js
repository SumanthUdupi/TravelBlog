export function getContent(path, defaultValue = 'Content not found') {
  const keys = path.split('.');
  let content = contentCopy;

  for (const key of keys) {
    if (content && typeof content === 'object' && key in content) {
      content = content[key];
    } else {
      return defaultValue;
    }
  }

  return content;
}

export function getLocalizedContent(path, locale = 'en') {
  // Future: Support for multi-language content
  if (locale !== 'en') {
    console.warn(`Locale ${locale} not yet supported. Falling back to English.`);
  }
  return getContent(path);
}
